 private void habilitaControles(boolean b) {
        JButton[] btn = {
            btnEliminar, btnNuevo, btnSalir, btnModificar
        };
        for (JButton bton : btn) {
            bton.setEnabled(!b);
        }
        btnGrabar.setEnabled(b);
        btnCancelar.setEnabled(b);

        JTextField[] txt = {
            txtDni, txtApellidos, txtDireccion, txtEmail, txtNombres, txtTelefono
        };
        for (JTextField txtx : txt) {
            txtx.setEditable(b);
        }
        txtDni.grabFocus();
    }

