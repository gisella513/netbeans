    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        try {
            empleadoTo to = new empleadoTo();
            String mensaje = "";
            to.setDni(String.valueOf(txtDni.getText()));
            to.setApellidos(String.valueOf(txtApellidos.getText()));
            to.setNombres(String.valueOf(txtNombres.getText()));
            to.setDireccion(String.valueOf(txtDireccion.getText()));
            to.setEmail(String.valueOf(txtEmail.getText()));
            to.setTelefono(String.valueOf(txtTelefono.getText()));
            if (sw) {
                emplaDao.RegistraEmpleado(to);
                mensaje = "Empleado Registrado...";
            } else {
                to.setCodigo(txtCodigo.getText());
                emplaDao.ModificarEmpleado(to);
                mensaje = "SE modifocaron los datos Correctamente...";
            }
            JOptionPane.showMessageDialog(rootPane, mensaje);
            habilitaControles(false);
            limpiaControles();
//            getRootPane().setDefaultButton(btnNuevo);
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnGrabarActionPerformed

