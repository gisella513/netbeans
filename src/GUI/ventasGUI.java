    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            model = (DefaultTableModel) tblDetalleFact.getModel();
            String fechaIcinio = dateFechaInicial.getText();
            String fechaFinal = dateFechaFinal.getText();
            rs = cajDao.reportarVentas(fechaIcinio, fechaFinal);
            while (rs.next()) {
                Object[] reg = {
                    rs.getString(1), rs.getString(2), rs.getString(3), rs.getDouble(4), rs.getDouble(5), rs.getDouble(6)
                };
                model.addRow(reg);
            }
//            calcularTotales();
            btnBuscar.setEnabled(false);
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

