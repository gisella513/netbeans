    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed

        List lista = new ArrayList();
        for (int i = 0; i < tblDetalleFact.getRowCount(); i++) {
            ventasReport listaventas = new ventasReport(tblDetalleFact.getValueAt(i, 0).toString(),
                    tblDetalleFact.getValueAt(i, 1).toString(), tblDetalleFact.getValueAt(i, 2).toString(),
                    Double.parseDouble(tblDetalleFact.getValueAt(i, 3).toString()), Double.parseDouble(tblDetalleFact.getValueAt(i, 4).toString()),
                    Double.parseDouble(tblDetalleFact.getValueAt(i, 5).toString()));
            lista.add(listaventas);
        }

        try {
            JasperReport report = (JasperReport) JRLoader.loadObject("VentasReport.jasper");
            Map parametro = new HashMap();
            parametro.put("FechaInicial", dateFechaInicial.getText());
            parametro.put("FechaFinal", dateFechaFinal.getText());
            JasperPrint print = JasperFillManager.fillReport(report, parametro, new JRBeanCollectionDataSource(lista));
            JasperViewer.viewReport(print);

        } catch (Exception e) {
            System.out.println(e.getMessage());
        }


    }//GEN-LAST:event_btnImprimirActionPerformed

